'use strict';

const {useEffect, useRef} = require('react');

function useIsMountedRef(): {|current: boolean|} {
  const isMountedRef = useRef(true);

  useEffect(() => {
    return () => {
      isMountedRef.current = false;
    };
  }, []);

  return isMountedRef;
}

module.exports = useIsMountedRef;
